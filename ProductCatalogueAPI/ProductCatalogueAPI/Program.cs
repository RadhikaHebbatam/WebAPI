using ProductCatalogueAPI.Extensions;
using Serilog;

// ?????????????????????????????????????????????????????????????
// BOOTSTRAP LOGGER
// WHY: We need logging available BEFORE the host builds.
// If startup fails (missing config, bad connection string),
// we capture that error. Without this, startup crashes are silent.
// ?????????????????????????????????????????????????????????????
Log.Logger = new LoggerConfiguration()
    .WriteTo.Console()
    .CreateBootstrapLogger();

try
{
    Log.Information("Starting ProductCatalogueAPI");

    AppContext.SetSwitch("Switch.Microsoft.Data.SqlClient.DisableUseOfAlias", true);

    var builder = WebApplication.CreateBuilder(args);

    // ?????????????????????????????????????????????????????????
    // SERILOG
    // WHY: Replaces default .NET logging with structured logging.
    // Default ILogger writes plain text.
    // Serilog writes structured JSON that Elasticsearch can index
    // and Kibana can query by individual field values.
    // ?????????????????????????????????????????????????????????
    builder.Host.UseSerilog((context, services, configuration) =>
        configuration
            .ReadFrom.Configuration(context.Configuration)
            .ReadFrom.Services(services)
            .Enrich.FromLogContext());

    // ?????????????????????????????????????????????????????????
    // KESTREL
    // WHY: Remove the Server header so we do not advertise
    // which web server software we are running.
    // Attackers use this to target known vulnerabilities.
    // ?????????????????????????????????????????????????????????
    builder.WebHost.ConfigureKestrel(options =>
    {
        options.AddServerHeader = false;
    });

    // ?????????????????????????????????????????????????????????
    // ALL SERVICES
    // One method call registers everything via our extension.
    // Program.cs stays clean � all wiring is in Extensions folder.
    // ?????????????????????????????????????????????????????????
    builder.Services.AddProductCatalogueServices(builder.Configuration);

    var app = builder.Build();

    // ?????????????????????????????????????????????????????????
    // MIDDLEWARE PIPELINE � order is critical
    // Each middleware wraps everything that comes after it.
    // ?????????????????????????????????????????????????????????

    // 1. Our custom middleware stack
    //    Correlation ID ? Exception Handler ? Logging ? Security Headers
    app.UseProductCatalogueMiddleware();

    // Static files (wwwroot) � serves the plain-English search frontend.
    // Must come before MapControllers so "/" resolves to wwwroot/index.html
    // instead of falling through to routing.
    app.UseDefaultFiles();
    app.UseStaticFiles();

    // 2. Swagger � development only

    app.UseSwagger();
    app.UseSwaggerUI();


    // 3. HSTS � tells browsers to use HTTPS only
    // WHY not in Development: dev uses self-signed certs
    if (!app.Environment.IsDevelopment())
    {
        app.UseHsts();
    }

    // 4. Force HTTP ? HTTPS
    app.UseHttpsRedirection();

    // 5. CORS � must be before Authentication
    // WHY: Browser preflight OPTIONS requests have no auth token.
    // CORS must run before auth to handle preflight correctly.
    app.UseCors("ProductCataloguePolicy");

    // 6. Authentication � who are you?
    // Wired up but JWT not configured until Phase 2
    app.UseAuthentication();

    // 7. Authorization � what can you do?
    app.UseAuthorization();

    // 8. Health check endpoints
    // WHY /health/live and /health/ready separately:
    // /health/live  = is the process running? (Kubernetes liveness)
    // /health/ready = is it ready for traffic? (Kubernetes readiness)
    app.MapHealthChecks("/health");
    app.MapHealthChecks("/health/live");
    app.MapHealthChecks("/health/ready");

    // 9. Controllers
    app.MapControllers();

    Log.Information("ProductCatalogueAPI started successfully");

    app.Run();
}
catch (Exception ex)
{
    // WHY: Catches fatal startup errors.
    // Database unreachable, missing config, bad connection string.
    // Without this they fail silently with no useful message.
    Log.Fatal(ex, "ProductCatalogueAPI failed to start");
}
finally
{
    // WHY: Ensures all buffered log entries are flushed
    // before the process exits. Without this you lose the
    // last few log lines on shutdown.
    Log.CloseAndFlush();
}